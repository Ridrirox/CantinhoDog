package br.com.Util;

/**
 *
 * @author Priscila
 */
public class DadosPassados {

    private Integer dado;
    private String data;
    private String setor;
    private String dataatual;
    private String NomeServidor;

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    /**
     * @return the setor
     */
    public String getSetor() {
        return setor;
    }

    /**
     * @param setor the setor to set
     */
    public void setSetor(String setor) {
        this.setor = setor;
    }

    /**
     * @return the dataatual
     */
    public String getDataatual() {
        return dataatual;
    }

    /**
     * @param dataatual the dataatual to set
     */
    public void setDataatual(String dataatual) {
        this.dataatual = dataatual;
    }

    /**
     * @return the NomeServidor
     */
    public String getNomeServidor() {
        return NomeServidor;
    }

    /**
     * @param NomeServidor the NomeServidor to set
     */
    public void setNomeServidor(String NomeServidor) {
        this.NomeServidor = NomeServidor;
    }

    /**
     * @return the dado
     */
    public Integer getDado() {
        return dado;
    }

    /**
     * @param dado the dado to set
     */
    public void setDado(Integer dado) {
        this.dado = dado;
    }

    
}
