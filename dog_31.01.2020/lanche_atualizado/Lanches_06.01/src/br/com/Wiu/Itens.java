package br.com.Wiu;

/**
 *
 * @author ROX
 */
public class Itens {
    
        private String cebola;
        private String milho;
        private String ervilha;
        private String batata;

    /**
     * @return the cebola
     */
    public String getCebola() {
        return cebola;
    }

    /**
     * @param cebola the cebola to set
     */
    public void setCebola(String cebola) {
        this.cebola = cebola;
    }

    /**
     * @return the milho
     */
    public String getMilho() {
        return milho;
    }

    /**
     * @param milho the milho to set
     */
    public void setMilho(String milho) {
        this.milho = milho;
    }

    /**
     * @return the ervilha
     */
    public String getErvilha() {
        return ervilha;
    }

    /**
     * @param ervilha the ervilha to set
     */
    public void setErvilha(String ervilha) {
        this.ervilha = ervilha;
    }

    /**
     * @return the batata
     */
    public String getBatata() {
        return batata;
    }

    /**
     * @param batata the batata to set
     */
    public void setBatata(String batata) {
        this.batata = batata;
    }
    
}
